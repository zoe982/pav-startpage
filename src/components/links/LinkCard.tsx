function getServiceIcon(url: string, title: string): string | null {
  const urlLower = url.toLowerCase();
  const titleLower = title.toLowerCase();
  for (const [key, value] of SERVICE_ICONS.entries()) {
    if (urlLower.includes(key) || titleLower.includes(key)) {
      return value;
    }
  }
  return null;
}

function SvgIcon({ name }: { readonly name: string }): JSX.Element | null {
  const path = ICON_PATHS.get(name);
  if (!path) return null;
  return (
    <svg
      viewBox="0 0 24 24"
      fill="currentColor"
      className="h-5 w-5 text-pav-terra"
      aria-hidden="true"
    >
      <path d={path} />
    </svg>
  );
}

function getFaviconUrl(url: string): string {
  try {
    const domain = new URL(url).hostname;
    return `https://www.google.com/s2/favicons?domain=${domain}&sz=64`;
  } catch {
    return '';
  }
}

export function LinkCard({ link }: { readonly link: Link }): JSX.Element {
  const iconName = getServiceIcon(link.url, link.title);
  const icon = iconName && ICON_PATHS.has(iconName) ? iconName : null;
  const favicon = !icon ? (link.iconUrl ?? getFaviconUrl(link.url)) : '';

  return (
    <a
      href={link.url}
      target="_blank"
      rel="noopener noreferrer"
      className="state-layer group flex flex-col gap-2 rounded-xl border border-pav-tan/30 bg-surface-container-lowest p-5 shadow-[var(--shadow-elevation-1)] motion-standard hover:border-pav-gold hover:shadow-[var(--shadow-elevation-2)]"
    >
      <div className="flex items-center gap-3">
        {icon ? (
          <div className="flex h-9 w-9 shrink-0 items-center justify-center rounded-lg bg-pav-gold/15">
            <SvgIcon name={icon} />
          </div>
        ) : favicon ? (
          <img
            src={favicon}
            alt=""
            className="h-8 w-8 rounded"
            loading="lazy"
          />
        ) : (
          <div className="flex h-9 w-9 shrink-0 items-center justify-center rounded-lg bg-pav-gold/15 text-sm font-bold text-pav-terra">
            {link.title.charAt(0).toUpperCase()}
          </div>
        )}
        <h3 className="text-sm font-semibold text-pav-blue group-hover:text-pav-terra">
          {link.title}
        </h3>
      </div>
      {link.description && (
        <p className="text-sm text-on-surface-variant">{link.description}</p>
      )}
    </a>
  );
}
