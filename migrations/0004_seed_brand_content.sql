A core rule of our formal, authoritative voice is to **never use the word "this" as a standalone pronoun**. It must always be followed by a noun (i.e., used as a determiner or adjective).

| Instead of | Use |
|---|---|
| "This is a mandatory rule." | "This rule is mandatory." |
| "This is why we exist." | "This belief is why we exist." |
| "We will handle this for you." | "We will handle this process for you." |

